// Theory: Variables are basic values or objects in languages which are stored in memory spaces.
// In JS, they can be declared in 3 ways:

// 1. Var file
var num = 10;
console.log(num);
var num = 20;
console.log(num);

// 2. Let file
let num2 = 10;
console.log(num2);
// num2 is not re-declared. Cause it shows errors

// 3. const file
const num3 = 10;
console.log(num3);
// num3 is not re-declared. Cause it shows errors